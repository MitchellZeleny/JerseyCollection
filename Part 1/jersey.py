# Class of jerseys and five attributes
class Jersey:
    def __init__(self, player, team, year, color, number):
        self.player = player
        self.team = team
        self.year = year
        self.color = color
        self.number = number


# Getter
def getPlayer(self):
    return self.player
def getTeam(self):
    return self.team
def getYear(self):
    return self.year
def getColor(self):
    return self.color
def getNumber(self):
    return self.number